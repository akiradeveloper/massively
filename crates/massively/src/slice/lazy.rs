impl<R, Left, Right, Op, Output> KernelColumn for TransformRead<R, (Left, Right), Op, Output>
where
    R: Runtime,
    Left: KernelColumn<Runtime = R, Item = Output> + KernelColumnAt<S0>,
    Right: KernelColumn<Runtime = R, Item = Output> + KernelColumnAt<S1>,
    Output: Scalar + 'static,
    Op: op::UnaryOp<R, (Output, Output), Output = (Output,)>,
    <Left as KernelColumnAt<S0>>::ExprAt: GpuExpr<Output> + DeviceGpuExpr<Output>,
    <Right as KernelColumnAt<S1>>::ExprAt: GpuExpr<Output> + DeviceGpuExpr<Output>,
{
    type Runtime = R;
    type Item = Output;
    type Expr = TransformExpr2<
        R,
        <Left as KernelColumnAt<S0>>::ExprAt,
        <Right as KernelColumnAt<S1>>::ExprAt,
        Op,
        Output,
    >;

    fn len(&self) -> usize {
        self.len
    }

    fn validate(&self) -> Result<(), Error> {
        self.source.0.validate()?;
        self.source.1.validate()?;
        crate::error::ensure_same_len(self.source.0.len(), self.source.1.len())
    }
}

