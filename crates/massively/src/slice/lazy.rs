impl<R, T> KernelColumnAt<S0> for ConstantRead<R, T>
where
    R: Runtime,
    T: Scalar + 'static,
{
    type ExprAt = ConstantExpr<T>;
    type Next = S1;

    fn stage_at(&self, bindings: &mut KernelColumnBindings) -> Result<(), Error> {
        bindings.push(self.value.handle.clone(), self.value.len);
        Ok(())
    }
}

