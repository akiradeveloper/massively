/// Lazy slice that returns the same value for every logical index.
#[derive(Clone, Copy, Debug)]
pub struct ConstantSlice<T> {
    len: usize,
    value: T,
}

/// Lazy slice that returns the logical index as `u32`.
#[derive(Clone, Copy, Debug)]
pub struct TabulateSlice {
    len: usize,
}

/// Lazy slice generated by transforming one logical item from an `MIter`.
#[derive(Debug)]
pub struct TransformSlice<R, Input, Op, Output>
where
    R: Runtime,
    Input: MIter<R>,
{
    len: usize,
    input: Input,
    _marker: PhantomData<fn() -> (R, Op, Output)>,
}

impl<R, Input, Op, Output> Clone for TransformSlice<R, Input, Op, Output>
where
    R: Runtime,
    Input: MIter<R> + Clone,
{
    fn clone(&self) -> Self {
        Self {
            len: self.len,
            input: self.input.clone(),
            _marker: PhantomData,
        }
    }
}

impl<R, Input, Op, Output> Copy for TransformSlice<R, Input, Op, Output>
where
    R: Runtime,
    Input: MIter<R> + Copy,
{
}

#[doc(hidden)]
#[derive(Clone)]
pub struct ConstantRead<R: Runtime, T> {
    len: usize,
    value: DeviceVec<R, T>,
}

#[doc(hidden)]
#[derive(Clone, Copy, Debug)]
pub struct TabulateRead<R: Runtime> {
    len: usize,
    _marker: PhantomData<fn() -> R>,
}

#[doc(hidden)]
#[derive(Clone, Copy, Debug)]
pub struct TransformRead<R, Source, Op, Output> {
    pub(crate) len: usize,
    pub(crate) source: Source,
    _marker: PhantomData<fn() -> (R, Op, Output)>,
}

#[doc(hidden)]
pub struct ConstantExpr<T>(PhantomData<fn() -> T>);

#[doc(hidden)]
pub struct TabulateExpr;

#[doc(hidden)]
pub struct TransformExpr<R, InputExpr, Op, Output>(PhantomData<fn() -> (R, InputExpr, Op, Output)>);

#[doc(hidden)]
pub struct TransformExpr2<R, LeftExpr, RightExpr, Op, Output>(
    PhantomData<fn() -> (R, LeftExpr, RightExpr, Op, Output)>,
);

#[doc(hidden)]
pub struct TransformExpr3<R, FirstExpr, SecondExpr, ThirdExpr, Op, Output>(
    PhantomData<fn() -> (R, FirstExpr, SecondExpr, ThirdExpr, Op, Output)>,
);

/// Creates a lazy slice that returns `value` for every index.
pub fn constant_slice<T>(len: usize, value: T) -> ConstantSlice<T>
where
    T: Scalar,
{
    ConstantSlice { len, value }
}

/// Creates a lazy `u32` slice whose value is its logical index.
pub fn tabulate_slice(len: usize) -> TabulateSlice {
    TabulateSlice { len }
}

/// Creates a lazy slice by applying `Op` to each logical item of `input`.
pub fn transform_slice<R, Input, Op, Output>(
    input: Input,
    _op: Op,
) -> TransformSlice<R, Input, Op, Output>
where
    R: Runtime,
    Input: MIter<R>,
    Output: Scalar,
    Op: op::UnaryOp<R, Input::Item, Output = (Output,)>,
{
    let len = input.len();
    TransformSlice {
        len,
        input,
        _marker: PhantomData,
    }
}

impl<R, T> crate::slice::MSlice<R> for ConstantSlice<T>
where
    R: Runtime,
    T: Scalar + 'static,
{
    type Item = T;
    type Read = ConstantRead<R, T>;

    fn len(&self) -> usize {
        self.len
    }

    fn validate_executor(&self, _exec: &Executor<R>) -> Result<(), Error> {
        Ok(())
    }

    fn into_read(self, policy: &crate::detail::CubePolicy<R>) -> Result<Self::Read, Error> {
        Ok(ConstantRead {
            len: self.len,
            value: policy.to_device(&[self.value])?,
        })
    }
}

