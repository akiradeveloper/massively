impl<R, Input, Op, Output> crate::slice::MSlice<R> for TransformSlice<R, Input, Op, Output>
where
    R: Runtime,
    Input: MIter<R>,
    Input::Item: MItem<R>,
    Output: Scalar + 'static,
    Op: op::UnaryOp<R, Input::Item, Output = (Output,)>,
    TransformRead<R, Input::Inner, Op, Output>:
        crate::detail::impls::IntoMaterializedColumn<R, Output>,
{
    type Item = Output;
    type Read = TransformRead<R, Input::Inner, Op, Output>;

    fn len(&self) -> usize {
        self.len
    }

    fn validate_executor(&self, _exec: &Executor<R>) -> Result<(), Error> {
        Ok(())
    }

    fn into_read(self, policy: &crate::detail::CubePolicy<R>) -> Result<Self::Read, Error> {
        Ok(TransformRead {
            len: self.len,
            source: self.input.into_inner_with_policy(policy)?,
            _marker: PhantomData,
        })
    }
}

